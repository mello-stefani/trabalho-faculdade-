nome = input("Digite o nome: ")  # string (texto)
idade = int(input("Digite a idade: ")) # inteiro (número sem vírgula)
altura = float(input("Digite sua altura: ")) # float (número com vírgula/ponto)


print(f"Seu nome é {nome}, tem {idade} anos e {altura:.2f} de altura.")
# :.2f faz com que o número tenha 2 casas decimais

