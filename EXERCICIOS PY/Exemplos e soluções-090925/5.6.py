'''
Desenvolva um algoritmo que leia números até
que a soma destes números seja maior do que 100.
Ao final, o algoritmo deverá exibir quantos números
foram lidos até que a condição de parada fosse atingida.
'''
soma = 0
conta = 0
while soma < 100 :
    soma += int(input("Informe um valor: "))
    conta += 1
print(f"Foram lidos {conta} numeros")

    
    
    
    
    
    
    


